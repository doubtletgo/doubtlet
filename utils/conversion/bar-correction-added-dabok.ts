function getTemperatureOnBarCorrectionAddedDabok(temperature, pressure) {
  const temp = parseFloat(temperature);
  const processedPressure = Math.round(pressure / 2) * 2;

  const tempRangeKey = Object.keys(temperaturePressureMap).find((range) => {
    const [minStr, maxStr] = range.replace('N', '-').split('-');
    const min = parseFloat(minStr);
    const max = parseFloat(maxStr);
    return temp >= min && temp <= max;
  });

  if (!tempRangeKey) {
    console.warn('No temperature range found for:', temp);
    return 'N/A';
  }

  return temperaturePressureMap[tempRangeKey]?.[processedPressure];
}

